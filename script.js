let date = new Date("July, 3, 2024 14:00:00")

console.log(date)

console.log("------------------")

// Modificar o ano.
date.setFullYear(2030)
console.log(date)

console.log("------------------")

// Modificar o mês (Começa com 0)
date.setMonth(8)
console.log(date)

console.log("------------------------")

// Modifica a hora 
date.setHours(18)
console.log(date)

console.log("------------------------")

date.setMinutes(23)
console.log(date)

console.log("------------------------")

date.setSeconds(30)
console.log(date)